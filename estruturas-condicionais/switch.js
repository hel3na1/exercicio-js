var diaSemana = 'segunda'
var dia

switch (diaSemana) {
  case 'segunda': 
    dia = 'segunda'
    break
  case 'terça': 
    dia = 'terça'
    break
  case 'quarta': 
    dia = 'quarta'
    break
  case 'quinta': 
    dia = 'quinta'
    break
  case 'sexta': 
    dia = 'sexta'
    break
  default: 
    dia = 'default'
    break
}

console.log(dia)